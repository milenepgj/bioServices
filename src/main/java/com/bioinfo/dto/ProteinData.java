package com.bioinfo.dto;

public class ProteinData {

    private String geneId;
    private String aminoacidData;

    public String getGeneId() {
        return geneId;
    }

    public void setGeneId(String geneId) {
        this.geneId = geneId;
    }

    public String getAminoacidData() {
        return aminoacidData;
    }

    public void setAminoacidData(String aminoacidData) {
        this.aminoacidData = aminoacidData;
    }
}
